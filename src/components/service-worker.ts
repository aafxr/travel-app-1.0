self.addEventListener("push", (e) => {
    const msg = e.data ? e.data.text() : ''
    if(msg){
        e.waitUntil(
            self.registration.showNotification('notification', {
                body: msg,
                vibrate: [500, 100, 500],
            })
        )
    }
});

