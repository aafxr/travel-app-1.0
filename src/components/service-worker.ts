const apiCache = [
    '/main/currency/getList/',
    '/expenses/getSections/'
]

registerRoute(
    ({url}) => url.origin === self.location.origin && apiCache.includes(url.pathname),
    new CacheFirst({
        cacheName: 'api',
        plugins: [ new ExpirationPlugin({maxAgeSeconds: 24 * 60 * 60}),],
    })
);

